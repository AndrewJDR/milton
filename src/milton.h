inline int32 absi(int32 a)
{
    return a < 0 ? -a : a;
}

inline int32 maxi(int32 a, int32 b)
{
    return a > b? a : b;
}

inline int32 mini(int32 a, int32 b)
{
    return a < b? a : b;
}


